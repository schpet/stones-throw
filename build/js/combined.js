function changeRadius(positive){
  if(positive){
    if(radius >= 1000){
      radius += 1000;
    } else {
      radius += 100;
    }
  } else {
    if(radius > 1000){
      radius -= 1000;
    } else if(radius > 100){
      radius -= 100;
    }
  }
  text = "";
  if(radius >= 1000){
    text = parseInt(radius / 1000, 10) + " km";
  } else {
    text = radius + " m";
  }

  $('#radius-feedback').text(text);
  searchTwitter();
}

